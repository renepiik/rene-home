<template>
    <!--
    <button @click="toggleTheme" aria-label="Toggle theme">

        <span v-if="this.theme == 'darkMode'"> (->) Light</span>
        <span v-else> (->) Dark</span>
    </button>
    -->
    <label class="switch">
        <input @change="toggleTheme" id="toggler" type="checkbox" aria-label="Toggle theme">
        <span class="slider round"></span>
    </label>
</template>

<script>
export default {
    name: "ThemeToggleButton",
    methods: {
        toggleTheme: function() {
            const theme = localStorage.getItem('theme') === 'darkMode' ? '' : 'darkMode';
            document.documentElement.setAttribute('data-theme', theme);
            localStorage.setItem('theme', theme);
        },
    },
    mounted() {
        let localTheme = localStorage.getItem('theme') === 'darkMode' ? 'darkMode' : '';
        document.documentElement.setAttribute('data-theme', localTheme);
    },
}
</script>

